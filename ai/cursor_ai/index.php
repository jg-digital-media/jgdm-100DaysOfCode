            <h2>About Cursor AI</h2>

            <p>Cursor AI is the leading Text Editor that bundles with AI to enhance your development experience by providing intelligent suggestions and actions based on your codebase.</p>

            <p>Using this AI technology, I've been able to build projects that I've previously never been able to do. For me, it's not that I don't understand programming concepts. It's more that I have a block in my head that prevents me from putting them together and solving problems. Things like Condition statements, event listeners, loops, functions, arguments and parameters. They're all in there to a certain extent in my head. I liken it to having the pieces of a jigsaw puzzle and simply not being able to work out what piece goes where. Ever.</p>
                
            <p>And this is after over 20 years now of working with programming in some form.</p>

            <h3 class = "l3-heading">And this is where &quot;Vibe Coding&quot; comes in.</h3>

            <p>I'm still learning about Vibe Coding as I go, but I recently realised that I've been doing it more and more without really knowing it was a thing. </p>
            
            <p>I said it was a "thing", but there's nothing official about the term. I don't think Computer Science approves or recommends the approach yet. But for people like me, though, it is a Godsend. I've always said, Vibe code, or use AI to code, with caution.  You know you rely on it to build projects, but you also know you do have to spend time studying the code and understanding that all that is happening with all these language models (LLMs) is that they are relying on assumptions. That is to say assumptions about your input and the data it scrapes online..

            <h3 class = "l3-heading">Pros and Cons of Vibe Coding</h3>

            <h4 class = "l4-heading" id="heading-pros">Pros:</h4>
            <ul>
                <li><strong>You can build projects that you've never been able to do before.</strong> This is an empowering feeling knowing that with the right tools you basically have an electronic collaborator by your side who will never have enough of your requests.</li>
                <li><strong>Confidence Boost: </strong> You don't have to fear the blank page — AI can help sketch things out, and you edit/steer as you go along.</li>
                <li><strong>Exploration-Friendly: </strong> You can test out ideas fast without worrying about doing it the “proper” way first.</li>
                <li><strong>Pair Programming Feel: </strong> Cursor and tools like ChatGPT and Anthropic's models can guide you through thought processes, edge cases, or “what next?” moments.</li>
                <li><strong>Speed & Flow: </strong> You can go from idea to working code super fast — even if you're not sure about the implementation.</li>
                <li><strong>Learning On the Go: </strong> AI can explain or suggest things as you build, which helps build intuition without deep-diving into documentation every time.</li>             
                
            </ul>

            <h4 class = "l4-heading" id="heading-cons">Cons:</h4>
            <ul>
                <li><strong>Shallow Understanding Risk: </strong> It's one thing to have code generated for you by your chosen AI. You have to spend time studying the code and understanding that it all it is doing is relying on assumptions. That is assumptions about your input and the data it scrapes online. I have felt myself going down this route plenty of times where I got something working the way I want and that's enough for me. That's a path to be wary of if you care about your code and good development.</li>
                <li><strong>Overconfidence Trap: </strong>AI-generated code can look right but be subtly wrong — you still need to review and reason through it.</li>
                <li><strong>Less Structure: </strong>If you're constantly improvising with the AI, your code can become patchy, inconsistent, or hard to scale.</li>
                <li><strong>AI Echo Chamber: </strong>If your prompts are vague or your direction's unclear, the AI might reinforce bad assumptions instead of challenge them.</li>

